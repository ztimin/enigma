function CARDTYPES(name, value) {
    Object.defineProperty(exports, name, {
        value: value,
        enumerable: true
    });
}

CARDTYPES("GTX_1060", "1060");
CARDTYPES("GTX_1070", "1070");
CARDTYPES("GTX_1070TI", "1070 Ti");
CARDTYPES("GTX_1080", "1080");
CARDTYPES("GTX_1080TI", "1080 Ti");
